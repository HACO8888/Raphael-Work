def deposit_table(p, r, years):
    print("Year      Amount on deposit")
    print("------------------------------------------")
    for y in range(1, years + 1):
        amount = p * ((1 + r) ** y)
        print("%4d%22.2f" % (y, amount))
    print("------------------------------------------")

deposit_table(1000, 0.05, 10)
