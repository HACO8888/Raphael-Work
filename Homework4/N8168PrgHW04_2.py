def bmi_status(h_cm, w_kg):
    h_m = h_cm / 100
    v = w_kg / (h_m ** 2)
    if v < 20:
        s = "太瘦"
    elif v <= 25:
        s = "正常"
    elif v <= 28:
        s = "過重"
    else:
        s = "肥胖"
    return v, s

h = float(input("請輸入身高(cm)："))
w = float(input("請輸入體重(kg)："))
v, s = bmi_status(h, w)
print("BMI = %.2f，體位判定：%s" % (v, s))
